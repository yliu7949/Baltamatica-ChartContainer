function chart = SankeyPlotExample()
%SankeyPlotExample Recreate slandarer demo6 with SankeyPlot.

    rootDir = fileparts(fileparts(mfilename('fullpath')));
    addpath(fullfile(rootDir, 'plot'));
    createScaledFigure(1.25);

    links = { ...
        'a1','A',1.2; 'a2','A',1; 'a1','B',0.6; 'a3','A',1; 'a3','C',0.5; ...
        'b1','B',0.4; 'b2','B',1; 'b3','B',1; 'c1','C',1; ...
        'c2','C',1; 'c3','C',1; 'A','AA',2; 'A','BB',1.2; ...
        'B','BB',1.5; 'B','AA',1.5; 'C','BB',2.3; 'C','AA',1.2};

    colors = [ ...
        65,140,240; 252,180,65; 224,64,10; 5,100,146; 191,191,191; ...
        26,59,105; 255,227,130; 18,156,221; 202,107,75; 0,92,219; ...
        243,210,136; 80,99,129; 241,185,168; 224,131,10; 120,147,190; ...
        127,91,93; 187,128,110; 197,173,143; 59,71,111; 104,95,126; ...
        76,103,86; 112,112,124; 72,39,24; 197,119,106; 160,126,88; ...
        238,208,146] ./ 255;

    chart = SankeyPlot(links, ...
        'Title', '', ...
        'CData', colors, ...
        'Arrow', 'on', ...
        'ShowTicks', true, ...
        'ShowTickLabels', true, ...
        'FontName', 'Cambria', ...
        'LabelFontSize', 17, ...
        'TickFontSize', 11, ...
        'LabelRadius', 1.65);
end

function createScaledFigure(scale)
%createScaledFigure Create a larger canvas for the scaled chord diagram.
    try
        fig = figure('Name', 'sankey demo6');
        oldUnits = get(fig, 'Units');
        set(fig, 'Units', 'pixels');
        position = get(fig, 'Position');
        position(3:4) = position(3:4) .* scale;
        set(fig, 'Position', position);
        set(fig, 'Units', oldUnits);
    catch
        figure('Name', 'sankey demo6');
    end
end
